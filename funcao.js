function calculadoraMedia(n1, n2) {
    return (n1 + n2) / 2;
}

function soma(n1, n2) {
    return n1 + n2;
}

function sub(n1, n2) {
    return n1 - n2;
}

 function mult(n1, n2) {
    return n1 * n2;
}

function div(n1, n2) {
    return n1 / n2;
}



